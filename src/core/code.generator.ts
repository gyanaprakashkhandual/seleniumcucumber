// Formatter
// ---------------------------------------------------------------------------

/**
 * Applies simple indentation normalisation to the generated Java source.
 *
 * The generator emits lines with hardcoded leading spaces which can fall
 * out of sync if nested blocks shift the expected indent level. This pass
 * re-calculates indentation from scratch by counting opening and closing
 * braces.
 *
 * @param code - Raw Java source string.
 * @returns Reformatted Java source string with consistent 4-space indentation.
 */
export function formatJavaCode(code: string): string {
  const INDENT_SIZE = 4;
  let indentLevel = 0;

  const formattedLines = code.split("\n").map((line) => {
    const trimmed = line.trim();

    if (!trimmed) {
      return "";
    }

    // Decrease indent before rendering closing braces
    if (trimmed === "}" || trimmed.startsWith("} ")) {
      indentLevel = Math.max(0, indentLevel - 1);
    }

    const indentedLine = " ".repeat(indentLevel * INDENT_SIZE) + trimmed;

    // Increase indent after lines that open a new block
    if (trimmed.endsWith("{")) {
      indentLevel++;
    }

    return indentedLine;
  });

  return formattedLines.join("\n");
}
