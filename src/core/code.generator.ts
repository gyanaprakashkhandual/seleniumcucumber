// Method-level emitters
// ---------------------------------------------------------------------------

/**
 * Generates the complete Javadoc + annotation + method body for a single step.
 *
 * If an identical camelCase name has already been used, a numeric suffix
 * (1, 2, 3 …) is appended to keep all method names unique within the class.
 *
 * @param parsedStep  - Structured step data produced by stepParser.
 * @param methodNames - Mutable set of already-claimed method names.
 * @param config      - Extension configuration.
 * @returns A string containing the full Java method source, indented at 4 spaces.
 */
function generateAdvancedStepDefinition(
  parsedStep: ParsedStep,
  methodNames: Set<string>,
  config: StepDefinitionConfig,
): string {
  // Resolve a unique method name by appending a counter when needed
  let uniqueMethodName = parsedStep.methodName;
  let counter = 1;
  while (methodNames.has(uniqueMethodName)) {
    uniqueMethodName = `${parsedStep.methodName}${counter}`;
    counter++;
  }
  methodNames.add(uniqueMethodName);

  const parametersList = parsedStep.parameters.join(", ");

  let method = "";

  // Javadoc block
  method += `    /**\n`;
  method += `     * Step: ${parsedStep.originalStep}\n`;
  if (parsedStep.parameters.length > 0) {
    method += `     * Parameters: ${parsedStep.parameters.length}\n`;
  }
  method += `     */\n`;

  // Cucumber annotation with regex pattern
  method += `    @${parsedStep.annotation}("^${parsedStep.parameterizedRegex}$")\n`;

  // Method signature
  method += `    public void ${uniqueMethodName}(${parametersList}) {\n`;

  // Try-catch wrapper with TODO comment and template body
  method += `        try {\n`;
  method += `            // TODO: Implement step logic for: ${parsedStep.stepText}\n`;

  // List extracted parameters as inline documentation
  if (parsedStep.parameters.length > 0) {
    method += `            // Available parameters:\n`;
    parsedStep.parameters.forEach((param) => {
      method += `            //   ${param}\n`;
    });
  }

  // Emit a contextual code scaffold based on annotation type and step text
  method += generateStepTemplate(parsedStep.annotation, parsedStep.stepText);

  method += `\n`;
  method += `            // Add assertions for verification\n`;
  method += `            // Assert.assertTrue("Step verification failed", condition);\n`;
  method += `\n`;
  method += `        } catch (Exception e) {\n`;
  method += `            throw new RuntimeException("Failed to execute step: ${parsedStep.stepText}", e);\n`;
  method += `        }\n`;
  method += `    }`;

  return method;
}

/**
 * Returns a short comment scaffold tailored to the step's annotation type
 * and common keywords found in the step text.
 *
 * The scaffold is intentionally left as comments so the developer can
 * uncomment and adapt rather than overwriting working code.
 *
 * @param annotation - "Given", "When", or "Then".
 * @param stepText   - The human-readable step text without the keyword.
 * @returns Indented comment lines ending with a newline.
 */
function generateStepTemplate(annotation: string, stepText: string): string {
  let template = "";
  const lower = stepText.toLowerCase();

  switch (annotation.toLowerCase()) {
    case "given":
      template += `            // Setup / precondition logic\n`;
      if (lower.includes("navigate") || lower.includes("open")) {
        template += `            // driver.get("URL");\n`;
      } else if (lower.includes("login") || lower.includes("user")) {
        template += `            // Perform login or user setup\n`;
      } else {
        template += `            // Initialise test data or application state\n`;
      }
      break;

    case "when":
      template += `            // Action / interaction logic\n`;
      if (lower.includes("click")) {
        template += `            // WebElement element = driver.findElement(By.id("elementId"));\n`;
        template += `            // element.click();\n`;
      } else if (lower.includes("enter") || lower.includes("input")) {
        template += `            // WebElement inputField = driver.findElement(By.id("inputId"));\n`;
        template += `            // inputField.sendKeys("value");\n`;
      } else if (lower.includes("select")) {
        template += `            // Select dropdown = new Select(driver.findElement(By.id("selectId")));\n`;
        template += `            // dropdown.selectByVisibleText("optionText");\n`;
      } else {
        template += `            // Perform the main action\n`;
      }
      break;

    case "then":
      template += `            // Verification / assertion logic\n`;
      if (lower.includes("should see") || lower.includes("displayed")) {
        template += `            // WebElement element = driver.findElement(By.id("elementId"));\n`;
        template += `            // Assert.assertTrue("Element should be displayed", element.isDisplayed());\n`;
      } else if (lower.includes("text") || lower.includes("contains")) {
        template += `            // String actualText = driver.findElement(By.id("elementId")).getText();\n`;
        template += `            // Assert.assertTrue("Text mismatch", actualText.contains("expectedText"));\n`;
      } else {
        template += `            // Verify the expected outcome\n`;
      }
      break;

    default:
      template += `            // Implement step logic\n`;
  }

  return template;
}

